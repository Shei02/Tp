    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
          if (modifica)
        {
          //recuperar aparato actual del arraylist
            Aparatos apaactual= Misaparatos.get(idactual);
            //pasar datos del formulario a apaactual
            int idaparato=Misaparatos.size();
        apaactual.Setnombre(txtnom.getText());
        apaactual.Setfuncion(txtfunc.getText());
        apaactual.Setedadmin(Integer.parseInt(txtedadmin.getText()));
        apaactual.Setedadmax(Integer.parseInt(txtedadmax.getText()));
        apaactual.Setbaja(baja.isSelected());
       
        JOptionPane.showMessageDialog(null,"Se ha modificado el aparato", "Atenci칩n" , JOptionPane.INFORMATION_MESSAGE);
        }
        
        else
        {
        if (txtnom.getText().equals("")||txtedadmax.getText().equals("")||txtedadmin.getText().equals("")||txtfunc.getText().equals(""))
        
           JOptionPane.showMessageDialog(null,"ERROR, datos incompletos", "Atenci칩n" , JOptionPane.INFORMATION_MESSAGE);
        
        else{  
       
        Aparatos nuevoaparato=new Aparatos();
        int idaparato=Misaparatos.size();
        nuevoaparato.Setnombre(txtnom.getText());
        nuevoaparato.Setfuncion(txtfunc.getText());
        nuevoaparato.Setedadmin(Integer.parseInt(txtedadmin.getText()));
        nuevoaparato.Setedadmax(Integer.parseInt(txtedadmax.getText()));
        nuevoaparato.Setbaja(baja.isSelected());
        nuevoaparato.Setid(idaparato);
       
        Misaparatos.add(nuevoaparato);
        System.out.println(Misaparatos.size());
        Limpiar();
        JOptionPane.showMessageDialog(null,"Nuevo aparato A침adido", "Atenci칩n" , JOptionPane.INFORMATION_MESSAGE);
          
        }
        }           
    }//GEN-LAST:event_jButton1ActionPerformed

